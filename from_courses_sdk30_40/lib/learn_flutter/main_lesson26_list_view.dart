import 'package:flutter/material.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'ListView',
      home: Scaffold(
        appBar: AppBar(
          title: const Text('Building List View'),
          centerTitle: true,
        ),
        body: const BodyListView(),
      ),
    );
  }
}

class BodyListView extends StatelessWidget {
  const BodyListView({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return ListView(
      padding: const EdgeInsets.all(8.0),
      // shrinkWrap: true, // затратная опция ведь он заставляет flutter постоянно подсчитывать размеры элементов
      // scrollDirection: Axis.horizontal,
      // itemExtent: 300,
      // reverse: true,
      children: const [
        ListTile(
          title: Text('Sun'),
          subtitle: Text('Today Clear'),
          leading: Icon(Icons.wb_sunny),
          trailing: Icon(Icons.keyboard_arrow_right),
        ),
        ListTile(
          title: Text('Cloudy'),
          subtitle: Text('Today Cloudy'),
          leading: Icon(Icons.wb_cloudy),
          trailing: Icon(Icons.keyboard_arrow_right),
        ),
        ListTile(
          title: Text('Snow'),
          subtitle: Text('Today Snow'),
          leading: Icon(Icons.ac_unit),
          trailing: Icon(Icons.keyboard_arrow_right),
        ),
      ],
    );
  }
}
